local function sortF(a, b)
	local aN = a:sub(7)
	local bN = b:sub(7)
	local aNasI = tonumber(aN)
	local bNasI = tonumber(bN)
	if aNasI ~= nil and bNasI ~= nil then
		return aNasI < bNasI
	else 
		return a < b
	end
end

local presets={}
local presetNames={}
for name, cloudPreset in pairs(clouds.presets) do
	if name:sub(-#"mac") ~= "mac" then
		presets[name] = cloudPreset
		table.insert(presetNames, name)
	end
end
table.sort(presetNames, sortF)

